package step01_기초;

/*
 * + 의 의미
 * 1. 더하기
 * 2. 연결 하기
 * 3. 단항연산자  (++a 같은거)
 */

public class SumTest {

	public static void main(String[] args) {
		 int a =5, b=7;
		 System.out.println(a);
		 System.out.println(b);
		 System.out.println(a+b);
		 System.out.println("a+b="+(a+b));	
		 System.out.println(++a);
		 System.out.println(a+"+"+b+"="+(a+b));	


	}

}
